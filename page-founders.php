                <p class="press-item-texte">La Galerie ORENDA constitue un espace inspiré du Nouveau Mexique où prédominent le bleu de Santa Fé et l’ ocre du désert.

La galerie accueille des peintres, sculpteurs, photographes, notamment l'oeuvre d'EDWARD CURTIS, joaillers, ainsi que des écrivains qui travaillent avec eux ou puisent leur inspiration aux mêmes sources.

Arts contemporains, arts amérindiens, arts émergents, tels sont les principaux axes de création représentés à la galerie

Artistes déjà connus, créateurs dont l'oeuvre est bien établie ailleurs (Santa Fe, Milan, New York, Canada, Pays basque, la Corse, pays de l'Est européen...) mais encore à découvrir à Paris, ou personnalités des autres mondes aux talents artistiques cachés sont exposés à la galerie ORENDA : des étoiles venues d'ailleurs.

Certaines expositions s’accompagnent de signatures d’ouvrages tandis que la galerie favorise les face à face d’artistes de styles ou d’univers différents, afin de suggérer ou de susciter des correspondances et des résonances.

Elle présente en priorité les artistes qui savent faire passer le souffle des grands espaces et des horizons lointains, une vision insolite des évènements de notre temps, et qui incarnent une ouverture au reste du monde. La Galerie ORENDA travaille en partenariat avec la galerie Stefano Cortina de Milan.
